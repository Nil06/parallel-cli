/**
 * Minimal i18n for the Parallel TUI. English is the canonical language;
 * agent-facing prompts stay in English (best for models), only the UI and
 * user-facing messages are localized.
 */
export type Lang = 'en' | 'zh' | 'es' | 'fr';

export const LANGS: { code: Lang; label: string }[] = [
  { code: 'en', label: 'English' },
  { code: 'zh', label: '中文（普通话）' },
  { code: 'es', label: 'Español' },
  { code: 'fr', label: 'Français' },
];

export const LANG_NAME_EN: Record<Lang, string> = {
  en: 'English',
  zh: 'Chinese (Mandarin)',
  es: 'Spanish',
  fr: 'French',
};

let current: Lang = 'en';
export function setLang(l: Lang): void {
  current = l;
}
export function getLang(): Lang {
  return current;
}

export function t(key: string, vars?: Record<string, string | number>): string {
  const s = STRINGS[current]?.[key] ?? STRINGS.en[key] ?? key;
  return vars ? s.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? '')) : s;
}

